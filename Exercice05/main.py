def sum(a, b):
    """
    Renvoie la somme de 2 nombres

    Args:
        a (number) : un nombre
        b (number) : un nombre

    Returns:
        la somme de a + b
    """
    return a + b


def subtraction(a, b):
    """
    Renvoie la soustraction de 2 nombres

    Args:
        a (number) : un nombre
        b (number) : un nombre

    Returns:
        la soustraction de a - b
    """
    return a - b
